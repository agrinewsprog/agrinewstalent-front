async function getPromotions(): Promise<Promotion[]> {
  try {
    const response = await fetch(`${process.env.NEXT_PUBLIC_API_URL}/promotions/active`, {
      cache: 'no-store',
      credentials: 'include',
    });

    if (!response.ok) {
      return [];
    }

    const data = await response.json();
    return data.data || [];
  } catch (error) {
    console.error('Error fetching promotions:', error);
    return [];
  }
}

